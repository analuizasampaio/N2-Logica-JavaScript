// Crie uma função que receba dois número e retorne a soma dos mesmos

// Definindo a funcao
function soma2numeros(num1,num2){
    return num1+num2
}

 //Note que queremos que a função retorne um valor, portanto colocamos um return

//  Chamando a função
soma2numeros(2,5)

//Para visualizar o resultado
console.log(soma2numeros(2,5))

//Podemos guardar o retorno da função em uma outra variável
let resultado = soma2numeros(2,5);
console.log(resultado)